fun main() {
    // ++ and --
    var number = 10
    println(number + 1)
    // or we can print like this
    number = ++number  // number = 10 + 1 = 11
    println(++number)  // number = 11 + 1 = 12

    println(number)  // 12
    println(number--)  // 12
    println(number)  // 11
    println(--number)  // 10

}